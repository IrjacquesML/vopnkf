/**
 * Obtenir une traduction
 * @param string $cle Clé de traduction
 * @param string $langue Code de langue (optionnel, utilise la langue de session)
 * @return string Texte traduit
 */
function __($cle, $langue = null) {
    global $traductions_interface;
    
    // Utiliser la langue de session si non spécifiée
    if ($langue === null) {
        $langue = $_SESSION['langue_preferee'] ?? 'fr';
    }
    
    // Retourner la traduction si elle existe
    if (isset($traductions_interface[$cle][$langue])) {
        return $traductions_interface[$cle][$langue];
    }
    
    // Retourner le français par défaut
    if (isset($traductions_interface[$cle]['fr'])) {
        return $traductions_interface[$cle]['fr'];
    }
    
    // Retourner la clé si aucune traduction n'existe
    return $cle;
}

/**
 * Afficher une traduction (raccourci pour echo __)
 * @param string $cle Clé de traduction
 * @param string $langue Code de langue (optionnel)
 */
function _e($cle, $langue = null) {
    echo __($cle, $langue);
}
